class Manager extends Employee{
	String managerType;
	
	Manager(){
		super();
		managerType = null;
	}
	
	Manager(String fullName, double salary, String managerType){
		super(fullName, salary);
		this.managerType = managerType;
	}
	
	void increaseSalary(double increment){
		salary = salary + increment;
	}
	
	String getDescription(){
		return("Manager name : "+fullName+" who is "+managerType+" having salary : "+salary);
	}
	
	void setManagerType(String managerType){
		this.managerType = managerType;
	}
}
