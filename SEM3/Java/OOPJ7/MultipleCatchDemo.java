class MultipleCatchDemo{
	public static void main(String args[]){
		int a[] = {1,2,3,4,5};
		int c;
		
		try{
			c = a[5]/0;
		}
		catch(ArrayIndexOutOfBoundsException e2){
			System.out.println(e2);
		}
		catch(IndexOutOfBoundsException e1){
			System.out.println(e1);
		}
		catch(Exception e3){
			System.out.println(e3);
		}
	}
}
