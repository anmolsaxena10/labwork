import java.util.Scanner;

class FirstTwo{
	public static void main(String args[]){
		Scanner sc = new Scanner(System.in);
		
		String s = sc.nextLine();
		if(s.length() == 1){
			System.out.println(s);
		}
		else if(s.equals("")){
			System.out.println("");
		}
		else{
			System.out.println(s.charAt(0)+""+s.charAt(1));
		}
	}
}
